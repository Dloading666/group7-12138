-- ====================================================
-- 教学模块 - 发票信息表
-- ====================================================

-- 发票信息表
DROP TABLE IF EXISTS `teach_invoice`;
CREATE TABLE `teach_invoice` (
                                 `id` BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT '主键ID',
                                 `tax_no` VARCHAR(50) NOT NULL COMMENT '税号',
                                 `taxpayer_id` VARCHAR(50) NOT NULL COMMENT '纳税人识别号（统一社会信用代码）',
                                 `sign` VARCHAR(10) NOT NULL COMMENT '发票类型（销项/进项）',
                                 `state` VARCHAR(10) NOT NULL COMMENT '发票状态（正常/作废/红冲）',
                                 `invoice_time` DATETIME NOT NULL COMMENT '发票开票时间',
                                 `jshj` DECIMAL(20, 5) NOT NULL DEFAULT 0.00000 COMMENT '价税合计',
                                 `invoice_no` VARCHAR(50) DEFAULT NULL COMMENT '发票号码',
                                 `invoice_code` VARCHAR(50) DEFAULT NULL COMMENT '发票代码',
                                 `create_by` BIGINT(20) DEFAULT NULL COMMENT '创建人',
                                 `create_time` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
                                 `update_by` BIGINT(20) DEFAULT NULL COMMENT '更新人',
                                 `update_time` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
                                 `deleted` TINYINT(1) DEFAULT 0 COMMENT '删除标志（0-未删除 1-已删除）',
                                 `remark` VARCHAR(500) DEFAULT NULL COMMENT '备注',
                                 PRIMARY KEY (`id`),
                                 KEY `idx_tax_no` (`tax_no`),
                                 KEY `idx_taxpayer_id` (`taxpayer_id`),
                                 KEY `idx_sign` (`sign`),
                                 KEY `idx_state` (`state`),
                                 KEY `idx_invoice_time` (`invoice_time`),
                                 KEY `idx_composite` (`tax_no`, `taxpayer_id`, `sign`, `state`, `invoice_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='发票信息表（教学用）';

